function heading(text, level) {
  return new Paragraph({ heading: level, children: [new TextRun(text)] });
}

function para(text, opts = {}) {
  return new Paragraph({
    spacing: { after: 120 },
    alignment: opts.align || AlignmentType.RIGHT,
    bidirectional: true,
    children: [new TextRun({ text, bold: opts.bold, size: opts.size || 24, font: "Arial", italics: opts.italics })]
  });
}

function bulletItem(text, ref) {
  return new Paragraph({
    numbering: { reference: ref, level: 0 },
    spacing: { after: 80 },
    alignment: AlignmentType.RIGHT,
    bidirectional: true,
    children: [new TextRun({ text, size: 24, font: "Arial" })]
  });
}

function sectionTitle(text) {
  return new Paragraph({
    spacing: { before: 360, after: 200 },
    alignment: AlignmentType.RIGHT,
    bidirectional: true,
    border: { bottom: { style: BorderStyle.SINGLE, size: 6, color: accentColor, space: 4 } },
    children: [new TextRun({ text, bold: true, size: 36, font: "Arial", color: accentColor })]
  });
}

function subTitle(text) {
  return new Paragraph({
    spacing: { before: 240, after: 120 },
    alignment: AlignmentType.RIGHT,
    bidirectional: true,
    children: [new TextRun({ text, bold: true, size: 28, font: "Arial", color: "2E86C1" })]
  });
}

function twoColRow(label, value, shaded) {
  const fill = shaded ? lightBg : "FFFFFF";
  return new TableRow({
    children: [
      new TableCell({
        borders, width: { size: 2800, type: WidthType.DXA },
        shading: { fill: medBg, type: ShadingType.CLEAR },
        margins: cellMargins,
        children: [new Paragraph({ alignment: AlignmentType.RIGHT, bidirectional: true, children: [new TextRun({ text: label, bold: true, size: 22, font: "Arial" })] })]
      }),
      new TableCell({
        borders, width: { size: 6560, type: WidthType.DXA },
        shading: { fill, type: ShadingType.CLEAR },
        margins: cellMargins,
        children: [new Paragraph({ alignment: AlignmentType.RIGHT, bidirectional: true, children: [new TextRun({ text: value, size: 22, font: "Arial" })] })]
      })
    ]
  });
}

function infoTable(rows) {
  return new Table({
    width: { size: 9360, type: WidthType.DXA },
    columnWidths: [2800, 6560],
    rows: rows.map((r, i) => twoColRow(r[0], r[1], i % 2 === 0))
  });
}

